int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int temp;

	temp = 0;
	while (temp <= n)
	{
		while (s1[n] && s2[n] == s1[n])
			n++;
	}
	return (s1[n] - s2[n]);
}
