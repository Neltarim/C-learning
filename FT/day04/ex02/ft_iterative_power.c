int		ft_iterative_power(int nb, int power)
{
	int result;
	int i;

	i = 1;
	result = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	else
	{
		while (i < power)
		{
			result *= nb;
			i++;
		}
		return (result);
	}
}
